// Hardcoded direction-number tables normalized to one common representation.
//
// Each entry stores:
//   d    : dimension number used for lookup
//   p    : prime base, currently 2 or 3
//   s    : polynomial degree; s == 0 means identity generator matrix
//   poly : lower polynomial coefficients, in increasing degree order:
//          P(x) = x^s + poly[s-1] x^(s-1) + ... + poly[1] x + poly[0]
