//
// The tables below are normalized from several raw formats:
//   - Joe-Kuo GF(2) and OneTwo GF(2): binary 'a' excluding the implicit constant 1.
//   - Faure-Lemieux IS-dec GF(2): binary lower coefficients including constant term.
//   - QuadQuad GF(3): base-3 polynomial integer including the leading coefficient;
